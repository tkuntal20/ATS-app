import jsPDF from 'jspdf';
import type { AnalysisResult, OptimizedResume, ExperienceEntry, EducationEntry, CertificationEntry, ProjectEntry } from '../types';

// ============================================================
// TYPOGRAPHY & STYLING CONSTANTS
// ============================================================

const FONTS = {
  regular: 'helvetica',
  bold: 'helvetica',
  italic: 'helvetica',
} as const;

const COLORS = {
  primary: [37, 99, 235] as [number, number, number],
  primaryDark: [29, 78, 216] as [number, number, number],
  accent: [147, 51, 234] as [number, number, number],
  green: [22, 163, 74] as [number, number, number],
  greenLight: [240, 253, 244] as [number, number, number],
  red: [220, 38, 38] as [number, number, number],
  redLight: [254, 242, 242] as [number, number, number],
  orange: [251, 146, 60] as [number, number, number],
  gray50: [249, 250, 251] as [number, number, number],
  gray100: [243, 244, 246] as [number, number, number],
  gray200: [229, 231, 235] as [number, number, number],
  gray400: [156, 163, 175] as [number, number, number],
  gray500: [107, 114, 128] as [number, number, number],
  gray600: [75, 85, 99] as [number, number, number],
  gray700: [55, 65, 81] as [number, number, number],
  gray800: [31, 41, 55] as [number, number, number],
  gray900: [17, 24, 39] as [number, number, number],
  white: [255, 255, 255] as [number, number, number],
  black: [0, 0, 0] as [number, number, number],
};

const MARGIN = 20;
const PAGE_WIDTH = 210;
const PAGE_HEIGHT = 297;
const CONTENT_WIDTH = PAGE_WIDTH - 2 * MARGIN;

// ============================================================
// PDF UTILITY FUNCTIONS
// ============================================================

function createDoc(): jsPDF {
  return new jsPDF({
    orientation: 'portrait',
    unit: 'mm',
    format: 'a4',
    compress: true,
  });
}

function addDivider(doc: jsPDF, y: number, color: [number, number, number] = COLORS.gray200): number {
  doc.setDrawColor(color[0], color[1], color[2]);
  doc.setLineWidth(0.3);
  doc.line(MARGIN, y, PAGE_WIDTH - MARGIN, y);
  return y + 3;
}

function checkPage(doc: jsPDF, y: number, neededSpace: number = 20): number {
  if (y + neededSpace > PAGE_HEIGHT - 25) {
    doc.addPage();
    return MARGIN;
  }
  return y;
}

function addFooter(doc: jsPDF, pageNum: number, totalPages: number, title: string) {
  const y = PAGE_HEIGHT - 10;
  doc.setFontSize(7);
  doc.setTextColor(COLORS.gray400[0], COLORS.gray400[1], COLORS.gray400[2]);
  doc.setFont(FONTS.regular, 'normal');

  doc.setDrawColor(COLORS.gray200[0], COLORS.gray200[1], COLORS.gray200[2]);
  doc.setLineWidth(0.3);
  doc.line(MARGIN, y - 2, PAGE_WIDTH - MARGIN, y - 2);

  doc.text(title, MARGIN, y + 2);
  doc.text(`Page ${pageNum} of ${totalPages}`, PAGE_WIDTH - MARGIN, y + 2, { align: 'right' });
  doc.text('Generated by ATS Resume Analyzer', PAGE_WIDTH / 2, y + 2, { align: 'center' });
}

function addSectionHeader(doc: jsPDF, y: number, title: string, color: [number, number, number] = COLORS.primary): { y: number } {
  y = checkPage(doc, y, 15);
  y += 3;

  doc.setFillColor(color[0], color[1], color[2]);
  doc.rect(MARGIN, y - 4, CONTENT_WIDTH, 8, 'F');

  doc.setTextColor(255, 255, 255);
  doc.setFontSize(10);
  doc.setFont(FONTS.bold, 'bold');
  doc.text(title.toUpperCase(), MARGIN + 4, y + 1);

  return { y: y + 10 };
}

function addWrappedText(doc: jsPDF, y: number, text: string, opts: {
  fontSize?: number;
  font?: string;
  fontStyle?: string;
  color?: [number, number, number];
  indent?: number;
  lineSpacing?: number;
  maxWidth?: number;
} = {}): number {
  const {
    fontSize = 9,
    font = FONTS.regular,
    fontStyle = 'normal',
    color = COLORS.gray800,
    indent = 0,
    lineSpacing = 4.5,
    maxWidth = CONTENT_WIDTH - indent,
  } = opts;

  doc.setFontSize(fontSize);
  doc.setFont(font, fontStyle);
  doc.setTextColor(color[0], color[1], color[2]);

  const lines = doc.splitTextToSize(text, maxWidth);
  lines.forEach((line: string) => {
    y = checkPage(doc, y, lineSpacing);
    doc.text(line, MARGIN + indent, y);
    y += lineSpacing;
  });

  return y;
}

function addBullet(doc: jsPDF, y: number, text: string, opts: {
  fontSize?: number;
  color?: [number, number, number];
  bulletChar?: string;
  indent?: number;
  lineSpacing?: number;
} = {}): number {
  const {
    fontSize = 9,
    color = COLORS.gray800,
    bulletChar = '\u2022',
    indent = 6,
    lineSpacing = 4.5,
  } = opts;

  doc.setFontSize(fontSize);
  doc.setFont(FONTS.regular, 'normal');

  const bulletWidth = doc.getTextWidth(bulletChar + ' ');
  const maxWidth = CONTENT_WIDTH - indent - bulletWidth - 2;
  const lines = doc.splitTextToSize(text, maxWidth);

  lines.forEach((line: string, idx: number) => {
    y = checkPage(doc, y, lineSpacing);
    if (idx === 0) {
      doc.setTextColor(COLORS.gray500[0], COLORS.gray500[1], COLORS.gray500[2]);
      doc.text(bulletChar, MARGIN + indent, y);
      doc.setTextColor(color[0], color[1], color[2]);
      doc.text(line, MARGIN + indent + bulletWidth + 1, y);
    } else {
      doc.setTextColor(color[0], color[1], color[2]);
      doc.text(line, MARGIN + indent + bulletWidth + 1, y);
    }
    y += lineSpacing;
  });

  return y;
}

function addScoreCircle(doc: jsPDF, cx: number, cy: number, score: number, radius: number = 18) {
  const color: [number, number, number] =
    score >= 80 ? COLORS.green : score >= 60 ? COLORS.orange : COLORS.red;

  doc.setDrawColor(COLORS.gray200[0], COLORS.gray200[1], COLORS.gray200[2]);
  doc.setLineWidth(0.5);
  doc.circle(cx, cy, radius, 'S');

  doc.setFillColor(color[0], color[1], color[2]);
  doc.circle(cx, cy, radius - 2, 'F');

  doc.setTextColor(255, 255, 255);
  doc.setFontSize(22);
  doc.setFont(FONTS.bold, 'bold');
  doc.text(score.toFixed(0), cx, cy + 1, { align: 'center' });

  doc.setFontSize(7);
  doc.setFont(FONTS.regular, 'normal');
  doc.text('/ 100', cx, cy + 8, { align: 'center' });
}

function addProgressBar(doc: jsPDF, y: number, label: string, value: number, max: number, barWidth: number = 80) {
  const percentage = (value / max) * 100;
  const barColor: [number, number, number] =
    percentage >= 80 ? COLORS.green : percentage >= 60 ? COLORS.orange : COLORS.red;

  doc.setFontSize(8);
  doc.setFont(FONTS.bold, 'bold');
  doc.setTextColor(COLORS.gray700[0], COLORS.gray700[1], COLORS.gray700[2]);
  doc.text(label, MARGIN, y);

  doc.setFont(FONTS.regular, 'normal');
  doc.setTextColor(COLORS.gray500[0], COLORS.gray500[1], COLORS.gray500[2]);
  doc.text(`${value.toFixed(1)} / ${max}`, MARGIN + 65, y);

  const barX = MARGIN + 95;
  const barY = y - 3;
  const barHeight = 4;

  doc.setFillColor(COLORS.gray200[0], COLORS.gray200[1], COLORS.gray200[2]);
  doc.roundedRect(barX, barY, barWidth, barHeight, 1.5, 1.5, 'F');

  doc.setFillColor(barColor[0], barColor[1], barColor[2]);
  const fillWidth = (barWidth * percentage) / 100;
  if (fillWidth > 0) {
    doc.roundedRect(barX, barY, fillWidth, barHeight, 1.5, 1.5, 'F');
  }

  doc.setFontSize(7);
  doc.setFont(FONTS.regular, 'normal');
  doc.setTextColor(COLORS.gray500[0], COLORS.gray500[1], COLORS.gray500[2]);
  doc.text(`${percentage.toFixed(0)}%`, barX + barWidth + 3, y + 0.5);
}

function getScoreColor(score: number): [number, number, number] {
  if (score >= 80) return COLORS.green;
  if (score >= 60) return COLORS.orange;
  return COLORS.red;
}

