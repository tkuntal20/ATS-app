// PDF 2: ATS ANALYSIS REPORT PDF
// ============================================================

export function generatePDFReport(
  results: AnalysisResult,
  optimizedResume?: OptimizedResume,
  optimizationMode?: string
) {
  const doc = createDoc();
  let y = MARGIN;

  // ===== HEADER =====
  doc.setFillColor(COLORS.primary[0], COLORS.primary[1], COLORS.primary[2]);
  doc.rect(0, 0, PAGE_WIDTH, 38, 'F');

  doc.setTextColor(255, 255, 255);
  doc.setFontSize(22);
  doc.setFont(FONTS.bold, 'bold');
  doc.text('ATS Resume Analysis Report', PAGE_WIDTH / 2, 16, { align: 'center' });

  doc.setFontSize(9);
  doc.setFont(FONTS.regular, 'normal');
  const reportDate = new Date().toLocaleDateString('en-US', {
    year: 'numeric', month: 'long', day: 'numeric',
  });
  doc.text(reportDate, PAGE_WIDTH / 2, 27, { align: 'center' });

  y = 48;

  // ===== OVERALL SCORE =====
  const scoreSection = addSectionHeader(doc, y, 'Overall ATS Score', COLORS.primary);
  y = scoreSection.y;

  const circleX = PAGE_WIDTH / 2;
  const circleY = y + 20;
  addScoreCircle(doc, circleX, circleY, results.score);
  y = circleY + 25;

  const message =
    results.score >= 80
      ? '✅ Excellent! Your resume is well-optimized for ATS systems. Recruiters will easily find your qualifications.'
      : results.score >= 60
      ? '👍 Good score! With a few targeted improvements, your resume can rank even higher with ATS systems.'
      : '📈 There is significant room for improvement. Follow the recommendations below to boost your ATS compatibility.';

  y = checkPage(doc, y, 10);
  doc.setFontSize(9);
  doc.setFont(FONTS.italic, 'italic');
  doc.setTextColor(COLORS.gray600[0], COLORS.gray600[1], COLORS.gray600[2]);
  const msgLines = doc.splitTextToSize(message, CONTENT_WIDTH - 10);
  msgLines.forEach((line: string) => {
    doc.text(line, PAGE_WIDTH / 2, y, { align: 'center' });
    y += 5;
  });
  y += 3;

  // ===== PREDICTED OPTIMIZED SCORE =====
  if (optimizedResume) {
    const optSection = addSectionHeader(doc, y, 'AI Optimization Result', COLORS.accent);
    y = optSection.y;

    doc.setFillColor(COLORS.accent[0], COLORS.accent[1], COLORS.accent[2]);
    doc.roundedRect(MARGIN, y - 3, 50, 6, 2, 2, 'F');
    doc.setTextColor(255, 255, 255);
    doc.setFontSize(7);
    doc.setFont(FONTS.bold, 'bold');
    doc.text(`Mode: ${(optimizationMode || 'balanced').toUpperCase()}`, MARGIN + 25, y + 0.5, { align: 'center' });
    y += 9;

    // Score comparison
    const origColor = getScoreColor(results.score);
    doc.setTextColor(origColor[0], origColor[1], origColor[2]);
    doc.setFontSize(28);
    doc.setFont(FONTS.bold, 'bold');
    doc.text(results.score.toFixed(0), MARGIN + 25, y + 8, { align: 'center' });
    doc.setFontSize(8);
    doc.setFont(FONTS.regular, 'normal');
    doc.setTextColor(COLORS.gray500[0], COLORS.gray500[1], COLORS.gray500[2]);
    doc.text('Original Score', MARGIN + 25, y + 14, { align: 'center' });

    doc.setFontSize(18);
    doc.setTextColor(COLORS.accent[0], COLORS.accent[1], COLORS.accent[2]);
    doc.text('\u2192', PAGE_WIDTH / 2, y + 9, { align: 'center' });

    doc.setFontSize(10);
    doc.setTextColor(COLORS.green[0], COLORS.green[1], COLORS.green[2]);
    doc.text(`+${optimizedResume.improvementPercentage.toFixed(0)}%`, PAGE_WIDTH / 2, y + 15, { align: 'center' });

    const optColor = getScoreColor(optimizedResume.predictedScore);
    doc.setTextColor(optColor[0], optColor[1], optColor[2]);
    doc.setFontSize(28);
    doc.setFont(FONTS.bold, 'bold');
    doc.text(optimizedResume.predictedScore.toFixed(0), PAGE_WIDTH - MARGIN - 25, y + 8, { align: 'center' });
    doc.setFontSize(8);
    doc.setFont(FONTS.regular, 'normal');
    doc.setTextColor(COLORS.gray500[0], COLORS.gray500[1], COLORS.gray500[2]);
    doc.text('Optimized Score', PAGE_WIDTH - MARGIN - 25, y + 14, { align: 'center' });

    y += 22;

    // Score improvement bar
    y = checkPage(doc, y, 15);
    const barTotalWidth = CONTENT_WIDTH;
    doc.setFillColor(COLORS.gray200[0], COLORS.gray200[1], COLORS.gray200[2]);
    doc.roundedRect(MARGIN, y, barTotalWidth, 6, 2, 2, 'F');

    const origBarX = MARGIN + (barTotalWidth * (results.score / 100));
    const optBarX = MARGIN + (barTotalWidth * (optimizedResume.predictedScore / 100));

    if (optBarX > origBarX) {
      doc.setFillColor(COLORS.green[0], COLORS.green[1], COLORS.green[2]);
      doc.roundedRect(origBarX, y, optBarX - origBarX, 6, 2, 2, 'F');
    }

    doc.setFillColor(origColor[0], origColor[1], origColor[2]);
    doc.circle(origBarX, y + 3, 3, 'F');
    doc.setTextColor(origColor[0], origColor[1], origColor[2]);
    doc.setFontSize(6);
    doc.text(results.score.toFixed(0), origBarX, y + 12, { align: 'center' });

    doc.setFillColor(optColor[0], optColor[1], optColor[2]);
    doc.circle(optBarX, y + 3, 3, 'F');
    doc.setTextColor(optColor[0], optColor[1], optColor[2]);
    doc.setFontSize(6);
    doc.text(optimizedResume.predictedScore.toFixed(0), optBarX, y + 12, { align: 'center' });

    y += 18;
  }

  // ===== SCORE BREAKDOWN =====
  if (results.breakdown) {
    const breakdownSection = addSectionHeader(doc, y, 'Score Breakdown', COLORS.primary);
    y = breakdownSection.y;

    const items = [
      { label: 'Keyword Match', score: results.breakdown.keywordMatch, max: 40 },
      { label: 'Skill Alignment', score: results.breakdown.skillAlignment, max: 25 },
      { label: 'Experience Match', score: results.breakdown.experienceMatch, max: 15 },
      { label: 'Formatting', score: results.breakdown.formatting, max: 10 },
      { label: 'Readability', score: results.breakdown.readability, max: 10 },
    ];

    items.forEach((item) => {
      y = checkPage(doc, y, 12);
      addProgressBar(doc, y, item.label, item.score, item.max);
      y += 9;
    });
    y += 2;
  }

  // ===== ADVANCED ANALYSIS =====
  if (optimizedResume?.analysis) {
    const advSection = addSectionHeader(doc, y, 'Advanced ATS Analysis', [16, 185, 129]);
    y = advSection.y;

    const metrics = [
      { label: 'Keyword Density', value: `${optimizedResume.analysis.keywordDensity.toFixed(1)}%`, target: 'Target: 2-5%' },
      { label: 'Readability', value: `${optimizedResume.analysis.readabilityScore.toFixed(0)}/100`, target: 'Higher is better' },
      { label: 'Action Verbs', value: `${optimizedResume.analysis.actionVerbScore.toFixed(0)}%`, target: '% strong verbs' },
      { label: 'Weak Bullets', value: `${optimizedResume.analysis.weakBullets.length}`, target: 'Ideally 0' },
    ];

    const cardW = (CONTENT_WIDTH - 8) / 2;
    const cardH = 18;

    metrics.forEach((metric, idx) => {
      const col = idx % 2;
      const row = Math.floor(idx / 2);
      const cardX = MARGIN + col * (cardW + 8);
      const cardY = y + row * (cardH + 6);

      doc.setFillColor(COLORS.greenLight[0], COLORS.greenLight[1], COLORS.greenLight[2]);
      doc.roundedRect(cardX, cardY, cardW, cardH, 3, 3, 'F');

      doc.setFontSize(18);
      doc.setFont(FONTS.bold, 'bold');
      doc.setTextColor(COLORS.green[0], COLORS.green[1], COLORS.green[2]);
      doc.text(metric.value, cardX + cardW / 2, cardY + 8, { align: 'center' });

      doc.setFontSize(8);
      doc.setFont(FONTS.regular, 'normal');
      doc.setTextColor(COLORS.gray600[0], COLORS.gray600[1], COLORS.gray600[2]);
      doc.text(metric.label, cardX + cardW / 2, cardY + 14, { align: 'center' });

      doc.setFontSize(6);
      doc.setTextColor(COLORS.gray400[0], COLORS.gray400[1], COLORS.gray400[2]);
      doc.text(metric.target, cardX + cardW / 2, cardY + cardH - 1, { align: 'center' });
    });

    y += 2 * (cardH + 6) + 5;

    // Section Completeness
    if (optimizedResume.analysis.sectionCompleteness) {
      y = checkPage(doc, y, 20);
      doc.setFontSize(9);
      doc.setFont(FONTS.bold, 'bold');
      doc.setTextColor(COLORS.gray700[0], COLORS.gray700[1], COLORS.gray700[2]);
      doc.text('Section Completeness', MARGIN, y);
      y += 5;

      const completeness = Object.entries(optimizedResume.analysis.sectionCompleteness);
      const compCardW = (CONTENT_WIDTH - 12) / 4;

      completeness.forEach(([section, complete]) => {
        const cardX = MARGIN + (0 * (compCardW + 4));
        doc.setFillColor(
          complete ? COLORS.greenLight[0] : COLORS.redLight[0],
          complete ? COLORS.greenLight[1] : COLORS.redLight[1],
          complete ? COLORS.greenLight[2] : COLORS.redLight[2]
        );
        doc.roundedRect(cardX, y, compCardW, 14, 2, 2, 'F');

        doc.setFontSize(7);
        doc.setFont(FONTS.bold, 'bold');
        doc.setTextColor(
          complete ? COLORS.green[0] : COLORS.red[0],
          complete ? COLORS.green[1] : COLORS.red[1],
          complete ? COLORS.green[2] : COLORS.red[2]
        );
        doc.text(section.charAt(0).toUpperCase() + section.slice(1), cardX + compCardW / 2, y + 5, { align: 'center' });

        doc.setFontSize(6);
        doc.setFont(FONTS.regular, 'normal');
        doc.text(complete ? '\u2713 Complete' : '\u2717 Missing', cardX + compCardW / 2, y + 11, { align: 'center' });
      });

      y += 20;
    }
  }

  // ===== MATCHED SKILLS =====
  if (results.matchedSkills.length > 0) {
    const section = addSectionHeader(doc, y, 'Matched Skills', COLORS.green);
    y = section.y;

    doc.setFontSize(8);
    doc.setFont(FONTS.regular, 'normal');
    doc.setTextColor(COLORS.gray800[0], COLORS.gray800[1], COLORS.gray800[2]);

    const skillsText = results.matchedSkills.join('  \u2022  ');
    const lines = doc.splitTextToSize(skillsText, CONTENT_WIDTH - 4);
    lines.forEach((line: string) => {
      y = checkPage(doc, y, 5);
      doc.text(line, MARGIN + 2, y);
      y += 5;
    });
    y += 2;
  }

  // ===== MISSING SKILLS =====
  if (results.missingSkills.length > 0) {
    const section = addSectionHeader(doc, y, 'Missing Skills', COLORS.red);
    y = section.y;

    doc.setFontSize(8);
    doc.setFont(FONTS.regular, 'normal');
    doc.setTextColor(COLORS.gray800[0], COLORS.gray800[1], COLORS.gray800[2]);

    const skillsText = results.missingSkills.join('  \u2022  ');
    const lines = doc.splitTextToSize(skillsText, CONTENT_WIDTH - 4);
    lines.forEach((line: string) => {
      y = checkPage(doc, y, 5);
      doc.text(line, MARGIN + 2, y);
      y += 5;
    });
    y += 2;
  }

  // ===== ADDED KEYWORDS =====
  if (optimizedResume?.addedKeywords && optimizedResume.addedKeywords.length > 0) {
    const section = addSectionHeader(doc, y, 'Keywords Added by AI', [99, 102, 241]);
    y = section.y;

    doc.setFontSize(8);
    doc.setFont(FONTS.regular, 'normal');
    doc.setTextColor(COLORS.gray800[0], COLORS.gray800[1], COLORS.gray800[2]);

    const kwText = optimizedResume.addedKeywords.join('  \u2022  ');
    const lines = doc.splitTextToSize(kwText, CONTENT_WIDTH - 4);
    lines.forEach((line: string) => {
      y = checkPage(doc, y, 5);
      doc.text(line, MARGIN + 2, y);
      y += 5;
    });
    y += 2;
  }

  // ===== STRENGTHS =====
  if (results.strengths.length > 0) {
    const section = addSectionHeader(doc, y, 'Strengths', COLORS.green);
    y = section.y;

    results.strengths.forEach((strength) => {
      y = addBullet(doc, y, strength, { color: COLORS.gray700, fontSize: 9 });
    });
    y += 1;
  }

  // ===== AREAS TO IMPROVE =====
  if (results.improvements.length > 0) {
    const section = addSectionHeader(doc, y, 'Areas to Improve', COLORS.accent);
    y = section.y;

    results.improvements.forEach((improvement) => {
      y = addBullet(doc, y, improvement, { color: COLORS.gray700, fontSize: 9 });
    });
    y += 1;
  }

  // ===== OPTIMIZATION IMPROVEMENTS =====
  if (optimizedResume?.improvements && optimizedResume.improvements.length > 0) {
    const section = addSectionHeader(doc, y, 'Optimization Improvements', [16, 185, 129]);
    y = section.y;

    optimizedResume.improvements.forEach((improvement) => {
      y = addBullet(doc, y, improvement, { color: COLORS.gray700, fontSize: 9 });
    });
    y += 1;
  }

  // ===== REWRITTEN BULLETS =====
  if (optimizedResume?.rewrittenBullets && optimizedResume.rewrittenBullets.length > 0) {
    const section = addSectionHeader(doc, y, 'Bullet Point Rewrites', COLORS.orange);
    y = section.y;

    optimizedResume.rewrittenBullets.slice(0, 10).forEach((rb) => {
      y = checkPage(doc, y, 25);

      doc.setFillColor(COLORS.redLight[0], COLORS.redLight[1], COLORS.redLight[2]);
      doc.roundedRect(MARGIN, y, CONTENT_WIDTH, 1, 2, 2, 'F');
      y += 2;

      doc.setFontSize(7);
      doc.setFont(FONTS.bold, 'bold');
      doc.setTextColor(COLORS.red[0], COLORS.red[1], COLORS.red[2]);
      doc.text('BEFORE:', MARGIN + 3, y);
      y += 4;

      doc.setFont(FONTS.regular, 'normal');
      doc.setTextColor(COLORS.gray600[0], COLORS.gray600[1], COLORS.gray600[2]);
      doc.setFontSize(8);
      const origLines = doc.splitTextToSize(rb.original, CONTENT_WIDTH - 10);
      origLines.forEach((line: string) => {
        doc.text(line, MARGIN + 8, y);
        y += 4;
      });
      y += 1;

      doc.setFillColor(COLORS.greenLight[0], COLORS.greenLight[1], COLORS.greenLight[2]);
      doc.roundedRect(MARGIN, y, CONTENT_WIDTH, 1, 2, 2, 'F');
      y += 2;

      doc.setFontSize(7);
      doc.setFont(FONTS.bold, 'bold');
      doc.setTextColor(COLORS.green[0], COLORS.green[1], COLORS.green[2]);
      doc.text('AFTER:', MARGIN + 3, y);
      y += 4;

      doc.setFontSize(8);
      doc.setFont(FONTS.regular, 'normal');
      doc.setTextColor(COLORS.gray800[0], COLORS.gray800[1], COLORS.gray800[2]);
      const optLines = doc.splitTextToSize(rb.optimized, CONTENT_WIDTH - 10);
      optLines.forEach((line: string) => {
        doc.text(line, MARGIN + 8, y);
        y += 4;
      });

      y += 1;
      doc.setFontSize(6.5);
      doc.setFont(FONTS.italic, 'italic');
      doc.setTextColor(COLORS.gray500[0], COLORS.gray500[1], COLORS.gray500[2]);
      const reasonLines = doc.splitTextToSize(`Why: ${rb.reason}`, CONTENT_WIDTH - 10);
      reasonLines.forEach((line: string) => {
        doc.text(line, MARGIN + 8, y);
        y += 3;
      });
      y += 3;
    });
  }

  // ===== RECOMMENDATIONS =====
  const recSection = addSectionHeader(doc, y, 'Recommendations', COLORS.primary);
  y = recSection.y;

  const recommendations: string[] = [
    'Incorporate missing skills naturally throughout your resume experience section',
    'Use keywords from the job description in your bullet points',
    'Ensure clean, ATS-friendly formatting (avoid tables, columns, graphics)',
    'Quantify achievements with specific metrics and numbers',
    'Lead bullet points with strong action verbs (achieved, implemented, optimized)',
  ];

  if (optimizedResume?.analysis) {
    if (optimizedResume.analysis.weakBullets.length > 0) {
      recommendations.push('Strengthen remaining weak bullet points with more impact-focused language');
    }
    if (optimizedResume.analysis.duplicateKeywords.length > 0) {
      recommendations.push('Reduce keyword repetition to avoid ATS keyword stuffing penalties');
    }
    if (optimizedResume.analysis.sectionCompleteness &&
        Object.values(optimizedResume.analysis.sectionCompleteness).some(v => !v)) {
      recommendations.push('Complete all resume sections for maximum ATS scoring potential');
    }
  }

  recommendations.forEach((rec) => {
    y = addBullet(doc, y, rec, { color: COLORS.gray700, fontSize: 9 });
  });

  // Footers
  const totalPages = doc.getNumberOfPages();
  for (let i = 1; i <= totalPages; i++) {
    doc.setPage(i);
    addFooter(doc, i, totalPages, 'ATS Resume Analyzer - Analysis Report');
  }

  const suffix = optimizedResume ? `_Optimized_${optimizationMode || 'ai'}` : '';
  const fileName = `ATS_Analysis_Report${suffix}_${new Date().toISOString().split('T')[0]}.pdf`;
  doc.save(fileName);
}

