export function generateComparisonPDF(
  originalResumeText: string,
  optimizedResume: OptimizedResume,
  originalScore: number,
  mode: string = 'balanced'
) {
  const doc = createDoc();
  let y = MARGIN;

  const LEFT_X = MARGIN;
  const RIGHT_X = PAGE_WIDTH / 2 + 5;
  const COL_WIDTH = PAGE_WIDTH / 2 - MARGIN - 8;

  // ===== HEADER =====
  doc.setFillColor(COLORS.primaryDark[0], COLORS.primaryDark[1], COLORS.primaryDark[2]);
  doc.rect(0, 0, PAGE_WIDTH, 42, 'F');

  doc.setFillColor(COLORS.red[0], COLORS.red[1], COLORS.red[2]);
  doc.rect(0, 42, PAGE_WIDTH / 2, 2.5, 'F');

  doc.setFillColor(COLORS.green[0], COLORS.green[1], COLORS.green[2]);
  doc.rect(PAGE_WIDTH / 2, 42, PAGE_WIDTH / 2, 2.5, 'F');

  doc.setTextColor(255, 255, 255);
  doc.setFontSize(16);
  doc.setFont(FONTS.bold, 'bold');
  doc.text('Before vs After Optimization', PAGE_WIDTH / 2, 16, { align: 'center' });

  doc.setFontSize(8);
  doc.setFont(FONTS.regular, 'normal');
  const modeLabel = mode.charAt(0).toUpperCase() + mode.slice(1);
  doc.text(
    `Mode: ${modeLabel} | ${new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })}`,
    PAGE_WIDTH / 2, 26, { align: 'center' }
  );

  doc.setFontSize(9);
  doc.setFont(FONTS.bold, 'bold');
  doc.setTextColor(255, 200, 200);
  doc.text(`Score: ${originalScore.toFixed(0)}/100`, PAGE_WIDTH / 4, 36, { align: 'center' });
  doc.setTextColor(200, 255, 200);
  doc.text(`Score: ${optimizedResume.predictedScore.toFixed(0)}/100`, (3 * PAGE_WIDTH) / 4, 36, { align: 'center' });

  doc.setFontSize(10);
  doc.setTextColor(255, 255, 200);
  doc.text(`+${optimizedResume.improvementPercentage.toFixed(0)}%`, PAGE_WIDTH / 2, 36, { align: 'center' });

  y = 50;

  // ===== SECTION HELPER FOR TWO-COLUMN LAYOUT =====
  const addComparisonSection = (
    title: string,
    leftContent: string,
    rightContent: string,
    options?: { fontSize?: number; fontWeight?: string }
  ) => {
    const fontSize = options?.fontSize || 8;
    const fontWeight = options?.fontWeight || 'normal';

    y = checkPage(doc, y, 15);
    doc.setFillColor(COLORS.gray100[0], COLORS.gray100[1], COLORS.gray100[2]);
    doc.rect(LEFT_X, y - 3, RIGHT_X + COL_WIDTH - LEFT_X, 8, 'F');

    doc.setFontSize(9);
    doc.setFont(FONTS.bold, 'bold');
    doc.setTextColor(COLORS.gray800[0], COLORS.gray800[1], COLORS.gray800[2]);
    doc.text(title, PAGE_WIDTH / 2, y + 2, { align: 'center' });
    y += 10;

    // Left column - Original
    y = checkPage(doc, y, 10);
    doc.setFontSize(7);
    doc.setFont(FONTS.bold, 'bold');
    doc.setTextColor(COLORS.red[0], COLORS.red[1], COLORS.red[2]);
    doc.text('ORIGINAL', LEFT_X, y);
    y += 4;

    doc.setFontSize(fontSize);
    doc.setFont(FONTS.regular, fontWeight);
    doc.setTextColor(COLORS.gray600[0], COLORS.gray600[1], COLORS.gray600[2]);
    const leftLines = doc.splitTextToSize(leftContent, COL_WIDTH - 2);
    leftLines.forEach((line: string) => {
      y = checkPage(doc, y, 4);
      doc.text(line, LEFT_X + 1, y);
      y += 4;
    });

    const leftEndY = y;

    // Right column - Optimized
    y = checkPage(doc, leftEndY, 10);
    doc.setFontSize(7);
    doc.setFont(FONTS.bold, 'bold');
    doc.setTextColor(COLORS.green[0], COLORS.green[1], COLORS.green[2]);
    doc.text('OPTIMIZED', RIGHT_X, y);
    y += 4;

    doc.setFontSize(fontSize);
    doc.setFont(FONTS.regular, fontWeight);
    doc.setTextColor(COLORS.gray800[0], COLORS.gray800[1], COLORS.gray800[2]);
    const rightLines = doc.splitTextToSize(rightContent, COL_WIDTH - 2);
    rightLines.forEach((line: string) => {
      y = checkPage(doc, y, 4);
      doc.text(line, RIGHT_X + 1, y);
      y += 4;
    });

    const rightEndY = y;
    y = Math.max(leftEndY, rightEndY) + 3;
    y = addDivider(doc, y, COLORS.gray200);
    y += 3;

    return y;
  };

  // Professional Summary
  if (optimizedResume.professionalSummary) {
    const origSummary = originalResumeText ? originalResumeText.substring(0, 500) : 'Original resume text not available';
    y = addComparisonSection('Professional Summary', origSummary, optimizedResume.professionalSummary, { fontSize: 8 });
  }

  // Skills
  if (optimizedResume.skills.length > 0) {
    const origSkills = originalResumeText
      ? originalResumeText.substring(0, 300)
      : 'Skills not extracted from original';
    const optSkills = optimizedResume.skills.join(', ');
    y = addComparisonSection(`Skills (${optimizedResume.skills.length} total)`, origSkills, optSkills, { fontSize: 8 });
  }

  // Experience Entries
  if (optimizedResume.experience.length > 0) {
    optimizedResume.experience.forEach((exp: ExperienceEntry) => {
      const optExpText = `${exp.title} at ${exp.company}\n${exp.dates}${exp.location ? ` | ${exp.location}` : ''}\n${exp.bullets.map(b => `\u2022 ${b}`).join('\n')}`;
      const origExpText = originalResumeText
        ? originalResumeText.substring(0, 500)
        : 'Original experience not available';

      y = addComparisonSection(
        `Experience: ${exp.title} at ${exp.company}`,
        origExpText, optExpText, { fontSize: 7.5 }
      );
    });
  }

  // Bullet Point Rewrites
  if (optimizedResume.rewrittenBullets.length > 0) {
    const rbTitle = addSectionHeader(doc, y, 'Bullet Point Improvements', COLORS.orange);
    y = rbTitle.y;

    optimizedResume.rewrittenBullets.slice(0, 12).forEach((rb) => {
      y = checkPage(doc, y, 20);

      doc.setFillColor(255, 247, 237);
      doc.rect(LEFT_X, y - 2, CONTENT_WIDTH, 7, 'F');
      doc.setFontSize(7);
      doc.setFont(FONTS.bold, 'bold');
      doc.setTextColor(COLORS.orange[0], COLORS.orange[1], COLORS.orange[2]);
      doc.text('Improvement', MARGIN + 3, y + 2);
      y += 8;

      // Original (left)
      doc.setFontSize(7);
      doc.setFont(FONTS.bold, 'bold');
      doc.setTextColor(COLORS.red[0], COLORS.red[1], COLORS.red[2]);
      doc.text('BEFORE:', LEFT_X, y);
      y += 3;

      doc.setFontSize(7.5);
      doc.setFont(FONTS.regular, 'normal');
      doc.setTextColor(COLORS.gray600[0], COLORS.gray600[1], COLORS.gray600[2]);
      const origLines = doc.splitTextToSize(rb.original, COL_WIDTH - 2);
      origLines.forEach((line: string) => {
        y = checkPage(doc, y, 3.5);
        doc.text(line, LEFT_X + 1, y);
        y += 3.5;
      });
      const leftY = y;

      // Optimized (right)
      y = Math.max(y - (origLines.length * 3.5), y - 20);
      doc.setFontSize(7);
      doc.setFont(FONTS.bold, 'bold');
      doc.setTextColor(COLORS.green[0], COLORS.green[1], COLORS.green[2]);
      doc.text('AFTER:', RIGHT_X, y);
      y += 3;

      doc.setFontSize(7.5);
      doc.setFont(FONTS.regular, 'normal');
      doc.setTextColor(COLORS.gray800[0], COLORS.gray800[1], COLORS.gray800[2]);
      const optLines = doc.splitTextToSize(rb.optimized, COL_WIDTH - 2);
      optLines.forEach((line: string) => {
        y = checkPage(doc, y, 3.5);
        doc.text(line, RIGHT_X + 1, y);
        y += 3.5;
      });
      const rightY = y;
      y = Math.max(leftY, rightY) + 2;

      // Reason
      doc.setFontSize(6.5);
      doc.setFont(FONTS.italic, 'italic');
      doc.setTextColor(COLORS.gray500[0], COLORS.gray500[1], COLORS.gray500[2]);
      const reasonLines = doc.splitTextToSize(`Why: ${rb.reason}`, CONTENT_WIDTH - 4);
      reasonLines.forEach((line: string) => {
        y = checkPage(doc, y, 3);
        doc.text(line, MARGIN + 2, y);
        y += 3;
      });

      y += 4;
      y = addDivider(doc, y, COLORS.gray200);
      y += 2;
    });
  }

  // ===== KEYWORDS & METRICS SUMMARY =====
  const summarySection = addSectionHeader(doc, y, 'Optimization Summary', COLORS.accent);
  y = summarySection.y;

  doc.setFontSize(9);
  doc.setFont(FONTS.bold, 'bold');
  doc.setTextColor(COLORS.gray800[0], COLORS.gray800[1], COLORS.gray800[2]);
  doc.text('Score:', LEFT_X, y);
  doc.setFont(FONTS.regular, 'normal');
  doc.setTextColor(COLORS.red[0], COLORS.red[1], COLORS.red[2]);
  doc.text(`${originalScore.toFixed(0)}/100`, LEFT_X + 20, y);
  doc.setTextColor(COLORS.gray500[0], COLORS.gray500[1], COLORS.gray500[2]);
  doc.text('\u2192', LEFT_X + 40, y);
  doc.setTextColor(COLORS.green[0], COLORS.green[1], COLORS.green[2]);
  doc.text(`${optimizedResume.predictedScore.toFixed(0)}/100`, LEFT_X + 50, y);
  doc.setTextColor(COLORS.accent[0], COLORS.accent[1], COLORS.accent[2]);
  doc.setFont(FONTS.bold, 'bold');
  doc.text(`+${optimizedResume.improvementPercentage.toFixed(0)}%`, LEFT_X + 75, y);
  y += 7;

  const metricsSummary = [
    `Keywords Added: ${optimizedResume.addedKeywords.length}`,
    `Bullets Rewritten: ${optimizedResume.rewrittenBullets.length}`,
    `Keyword Density: ${optimizedResume.analysis.keywordDensity.toFixed(1)}%`,
    `Action Verb Score: ${optimizedResume.analysis.actionVerbScore.toFixed(0)}%`,
  ];

  metricsSummary.forEach((metric) => {
    doc.setFontSize(8);
    doc.setFont(FONTS.regular, 'normal');
    doc.setTextColor(COLORS.gray700[0], COLORS.gray700[1], COLORS.gray700[2]);
    doc.text(`\u2022 ${metric}`, LEFT_X + 2, y);
    y += 4.5;
  });

  // Footers
  const totalPages = doc.getNumberOfPages();
  for (let i = 1; i <= totalPages; i++) {
    doc.setPage(i);
    addFooter(doc, i, totalPages, 'ATS Resume Analyzer - Before vs After Comparison');
  }

  const fileName = `before-after-comparison-${mode}-${new Date().toISOString().split('T')[0]}.pdf`;
  doc.save(fileName);
}

