// PDF 1: PROFESSIONAL OPTIMIZED RESUME PDF
// ============================================================

export function generateResumePDF(
  optimizedResume: OptimizedResume,
  mode: string = 'balanced'
) {
  const doc = createDoc();
  let y = MARGIN;

  // ===== HEADER =====
  doc.setFillColor(COLORS.primaryDark[0], COLORS.primaryDark[1], COLORS.primaryDark[2]);
  doc.rect(0, 0, PAGE_WIDTH, 35, 'F');

  doc.setFillColor(COLORS.accent[0], COLORS.accent[1], COLORS.accent[2]);
  doc.rect(0, 35, PAGE_WIDTH, 2, 'F');

  doc.setTextColor(255, 255, 255);
  doc.setFontSize(18);
  doc.setFont(FONTS.bold, 'bold');
  doc.text('Optimized Resume', PAGE_WIDTH / 2, 18, { align: 'center' });

  doc.setFontSize(8);
  doc.setFont(FONTS.regular, 'normal');
  const modeLabel = mode.charAt(0).toUpperCase() + mode.slice(1);
  doc.text(
    `Mode: ${modeLabel} | Predicted ATS Score: ${optimizedResume.predictedScore.toFixed(0)}/100 | ${new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })}`,
    PAGE_WIDTH / 2, 28, { align: 'center' }
  );

  y = 42;

  // Professional Summary
  if (optimizedResume.professionalSummary) {
    const section = addSectionHeader(doc, y, 'Professional Summary', COLORS.primary);
    y = section.y;
    y = addWrappedText(doc, y, optimizedResume.professionalSummary, {
      fontSize: 9, fontStyle: 'normal', lineSpacing: 4.5, indent: 2,
    });
    y += 2;
  }

  // Skills
  if (optimizedResume.skills.length > 0) {
    const section = addSectionHeader(doc, y, 'Skills', COLORS.primary);
    y = section.y;

    const skillFontSize = 8;
    doc.setFontSize(skillFontSize);
    doc.setFont(FONTS.bold, 'bold');
    const maxLineWidth = CONTENT_WIDTH - 4;
    const line: string[] = [];
    let lineWidth = 0;

    optimizedResume.skills.forEach((skill, idx) => {
      const separator = idx < optimizedResume.skills.length - 1 ? ', ' : '';
      const text = skill + separator;
      const w = doc.getTextWidth(text);

      if (lineWidth + w > maxLineWidth && line.length > 0) {
        y = checkPage(doc, y, 6);
        doc.setTextColor(COLORS.gray800[0], COLORS.gray800[1], COLORS.gray800[2]);
        doc.text(line.join(''), MARGIN + 2, y);
        y += 6;
        line.length = 0;
        lineWidth = 0;
      }
      line.push(text);
      lineWidth += w;
    });

    if (line.length > 0) {
      y = checkPage(doc, y, 6);
      doc.setTextColor(COLORS.gray800[0], COLORS.gray800[1], COLORS.gray800[2]);
      doc.text(line.join(''), MARGIN + 2, y);
      y += 6;
    }
    y += 1;
  }

  // Experience
  if (optimizedResume.experience.length > 0) {
    const section = addSectionHeader(doc, y, 'Experience', COLORS.primary);
    y = section.y;

    optimizedResume.experience.forEach((exp: ExperienceEntry) => {
      y = checkPage(doc, y, 20);

      doc.setFontSize(10);
      doc.setFont(FONTS.bold, 'bold');
      doc.setTextColor(COLORS.gray900[0], COLORS.gray900[1], COLORS.gray900[2]);
      doc.text(`${exp.title} at ${exp.company}`, MARGIN + 2, y);
      y += 5;

      doc.setFontSize(8);
      doc.setFont(FONTS.regular, 'normal');
      doc.setTextColor(COLORS.gray500[0], COLORS.gray500[1], COLORS.gray500[2]);
      let dateLocation = exp.dates;
      if (exp.location) dateLocation += ` | ${exp.location}`;
      doc.text(dateLocation, MARGIN + 2, y);
      y += 5;

      exp.bullets.forEach((bullet: string) => {
        y = addBullet(doc, y, bullet, { fontSize: 9, color: COLORS.gray800, indent: 2, lineSpacing: 4.5 });
      });

      if (exp.technologies && exp.technologies.length > 0) {
        y += 1;
        doc.setFontSize(7);
        doc.setFont(FONTS.italic, 'italic');
        doc.setTextColor(COLORS.gray400[0], COLORS.gray400[1], COLORS.gray400[2]);
        doc.text(`Technologies: ${exp.technologies.join(', ')}`, MARGIN + 8, y);
        y += 4;
      }
      y += 2;
    });
  }

  // Education
  if (optimizedResume.education.length > 0) {
    const section = addSectionHeader(doc, y, 'Education', COLORS.primary);
    y = section.y;

    optimizedResume.education.forEach((edu: EducationEntry) => {
      y = checkPage(doc, y, 12);

      doc.setFontSize(9);
      doc.setFont(FONTS.bold, 'bold');
      doc.setTextColor(COLORS.gray900[0], COLORS.gray900[1], COLORS.gray900[2]);
      let eduLine = edu.degree;
      if (edu.field) eduLine += ` in ${edu.field}`;
      doc.text(eduLine, MARGIN + 2, y);
      y += 4.5;

      doc.setFontSize(8);
      doc.setFont(FONTS.regular, 'normal');
      doc.setTextColor(COLORS.gray600[0], COLORS.gray600[1], COLORS.gray600[2]);
      let instLine = edu.institution;
      if (edu.dates) instLine += ` | ${edu.dates}`;
      if (edu.gpa) instLine += ` | GPA: ${edu.gpa}`;
      doc.text(instLine, MARGIN + 2, y);
      y += 4.5;

      if (edu.honors && edu.honors.length > 0) {
        doc.setFontSize(7);
        doc.setFont(FONTS.italic, 'italic');
        doc.setTextColor(COLORS.gray500[0], COLORS.gray500[1], COLORS.gray500[2]);
        doc.text(`Honors: ${edu.honors.join(', ')}`, MARGIN + 2, y);
        y += 3;
      }
      y += 2;
    });
  }

  // Certifications
  if (optimizedResume.certifications.length > 0) {
    const section = addSectionHeader(doc, y, 'Certifications', COLORS.primary);
    y = section.y;

    optimizedResume.certifications.forEach((cert: CertificationEntry) => {
      y = checkPage(doc, y, 8);

      doc.setFontSize(9);
      doc.setFont(FONTS.bold, 'bold');
      doc.setTextColor(COLORS.gray800[0], COLORS.gray800[1], COLORS.gray800[2]);
      doc.text(cert.name, MARGIN + 2, y);
      y += 4.5;

      doc.setFontSize(8);
      doc.setFont(FONTS.regular, 'normal');
      doc.setTextColor(COLORS.gray500[0], COLORS.gray500[1], COLORS.gray500[2]);
      let certLine = '';
      if (cert.issuer) certLine += cert.issuer;
      if (cert.date) certLine += certLine ? ` | ${cert.date}` : cert.date;
      if (cert.credentialId) certLine += certLine ? ` | ID: ${cert.credentialId}` : `ID: ${cert.credentialId}`;
      if (certLine) doc.text(certLine, MARGIN + 2, y);
      y += 4;
      y += 1;
    });
  }

  // Projects
  if (optimizedResume.projects.length > 0) {
    const section = addSectionHeader(doc, y, 'Projects', COLORS.primary);
    y = section.y;

    optimizedResume.projects.forEach((proj: ProjectEntry) => {
      y = checkPage(doc, y, 12);

      doc.setFontSize(9);
      doc.setFont(FONTS.bold, 'bold');
      doc.setTextColor(COLORS.gray900[0], COLORS.gray900[1], COLORS.gray900[2]);
      doc.text(proj.name, MARGIN + 2, y);
      y += 4.5;

      if (proj.description) {
        y = addWrappedText(doc, y, proj.description, {
          fontSize: 8, color: COLORS.gray600, indent: 2, lineSpacing: 4,
        });
      }

      if (proj.technologies.length > 0) {
        doc.setFontSize(7);
        doc.setFont(FONTS.italic, 'italic');
        doc.setTextColor(COLORS.gray400[0], COLORS.gray400[1], COLORS.gray400[2]);
        doc.text(`Technologies: ${proj.technologies.join(', ')}`, MARGIN + 8, y);
        y += 3.5;
      }

      proj.bullets.forEach((bullet: string) => {
        y = addBullet(doc, y, bullet, { fontSize: 8, color: COLORS.gray700, indent: 2, lineSpacing: 4 });
      });
      y += 2;
    });
  }

  // Footers
  const totalPages = doc.getNumberOfPages();
  for (let i = 1; i <= totalPages; i++) {
    doc.setPage(i);
    addFooter(doc, i, totalPages, 'ATS Resume Analyzer - Optimized Resume');
  }

  const fileName = `optimized-resume-${mode}-${new Date().toISOString().split('T')[0]}.pdf`;
  doc.save(fileName);
}

