export function generatePDF(options: { results: AnalysisResult; optimizedResume?: OptimizedResume; optimizationMode?: string } | AnalysisResult) {
  let results: AnalysisResult;
  let optimizedResume: OptimizedResume | undefined;
  let optimizationMode: string | undefined;

  if ('results' in options) {
    const opts = options as { results: AnalysisResult; optimizedResume?: OptimizedResume; optimizationMode?: string };
    results = opts.results;
    optimizedResume = opts.optimizedResume;
    optimizationMode = opts.optimizationMode;
  } else {
    results = options as AnalysisResult;
  }

  generatePDFReport(results, optimizedResume, optimizationMode);
}